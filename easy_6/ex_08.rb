# Practice Problem:
#   - Given an unordered array and the information that exactly one value in
#     the array occurs twice (every other value occurs exactly once), how
#     would you determine which value occurs twice?

#     Write a method that will find and return the duplicate value that is
#     known to be in the array.

# --------------------------------------------------------------------------- #

# --- (UNDERSTAND THE) PROBLEM --- #

#   - from an unordered array where one value occurs twice and every other
#     value occurs once, write a method that returns the value occuring twice.

#   input: array (unordered)
#   output: an object (from examples, an integer)

#   rules:
#     explicit:
#       - the array is unordered
#       - every value occurs once, except one value occuring twice
#       - return the value that occurs twice (duplicate)
#     implicit:
#       - we are working with integers
#       - we want a single element value from the array 

# --- EXAMPLES --- #

# find_dup([1, 5, 3, 1]) == 1
# find_dup([18,  9, 36, 96, 31, 19, 54, 75, 42, 15,
#           38, 25, 97, 92, 46, 69, 91, 59, 53, 27,
#           14, 61, 90, 81,  8, 63, 95, 99, 30, 65,
#           78, 76, 48, 16, 93, 77, 52, 49, 37, 29,
#           89, 10, 84,  1, 47, 68, 12, 33, 86, 60,
#           41, 44, 83, 35, 94, 73, 98,  3, 64, 82,
#           55, 79, 80, 21, 39, 72, 13, 50,  6, 70,
#           85, 87, 51, 17, 66, 20, 28, 26,  2, 22,
#           40, 23, 71, 62, 73, 32, 43, 24,  4, 56,
#           7,  34, 57, 74, 45, 11, 88, 67,  5, 58]) == 73


# --- DATA STRUCTURES --- #

#   - Array
#   - Integer

# --- ALGORITHM --- #

#   - with the first element being the current_element of an unordered array:
#     1 - iterate the remaining elements
#      2 - compare each element with the current_element
#       3 - if theres a match, return the current_element
#     - make the second element the current_element and repeat 1-3
#     - increment current_element until all elements searched or match returned

# --------------------------------------------------------------------------- #

# --- CODE --- #

# my solution
def find_dup1(arr)
  (0...(arr.size - 1)).each do |outer_idx|
    current_num = arr[outer_idx]
    ((outer_idx + 1)...arr.size).each do |inner_idx|
      return current_num if current_num == arr[inner_idx]
    end
  end
end

# best solution
def find_dup2(arr)
  arr.find { |e| arr.count(e) == 2 }
end

# another solution
def find_dup3(arr)
  arr.select { |e| arr.count(e) == 2 }.first
end

